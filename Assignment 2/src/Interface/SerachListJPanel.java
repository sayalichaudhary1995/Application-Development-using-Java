    public void populateTable()
    {
        DefaultTableModel tmodel = (DefaultTableModel) tblFilteredDetails.getModel();
        tmodel.setRowCount(0);
        for(Car car: refereshedSearch)
        {
            Object[] row = new Object[4];
            row[0] = car;
            row[1] = car.getmodelNo();
            row[2] = car.getAvailablity();
            row[3] = car.getPrice();
            
            tmodel.addRow(row);
        }
    }

